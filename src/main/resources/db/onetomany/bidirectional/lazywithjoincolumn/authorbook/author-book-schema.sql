CREATE TABLE author (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY,
  name VARCHAR(255),
  PRIMARY KEY (id)
);

CREATE TABLE book (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY,
  title VARCHAR(255),
  author_id BIGINT,
  PRIMARY KEY (id)
  );

ALTER TABLE IF EXISTS book
  ADD CONSTRAINT author_id_fk FOREIGN KEY (author_id) REFERENCES author;